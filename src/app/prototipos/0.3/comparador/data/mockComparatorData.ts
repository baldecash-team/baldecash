// Get product by ID
export const getProductById = (id: string): ComparisonProduct | undefined =>
  mockComparisonProducts.find((p) => p.id === id);

// Get random products for demo
export const getRandomProducts = (count: number): ComparisonProduct[] => {
  const shuffled = [...mockComparisonProducts].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};
